package MOD15COTS.geceeebangg;

public class Asesment {
    private String namaMhasiswa;
    private String nimMahasiswa;

    public Asesment(String namaMhasiswa, String nimMahasiswa) {
        this.namaMhasiswa = namaMhasiswa;
        this.nimMahasiswa = nimMahasiswa;
    }

    public String getNamaMhasiswa() {
        return namaMhasiswa;
    }

    public String getNimMahasiswa() {
        return nimMahasiswa;
    }

}
